% 采样率
fs = 2; % Hz

% 规格
Wp = 0.6 / (fs / 2); % 通带截止频率 (归一化)
Ws = 0.8 / (fs / 2); % 阻带截止频率 (归一化)
Rp = 3; % 通带最大衰减 (dB)
Rs = 20; % 阻带最小衰减 (dB)

% 设计巴特沃斯低通滤波器
[n, Wn] = buttord(Wp, Ws, Rp, Rs); % 计算滤波器阶数和截止频率
[b, a] = butter(n, Wn, 'low'); % 设计滤波器

% 绘制滤波器的幅频特性
freqz(b, a, 1024, fs);
title('巴特沃斯低通滤波器的幅频特性');
